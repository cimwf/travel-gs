#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const sourcePath = path.join(rootDir, 'shared/ai-image-error-rules.json');
const targetPaths = [
  'cloudfunctions/api/utils/ai-image-error-rules.js',
  'services/ai-image-service/src/ai-image-error-rules.js',
  'miniprogram/utils/ai-image-error-rules.js'
];

function readRuleSource() {
  return JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
}

function buildGeneratedModule(ruleSource) {
  return `// Generated by scripts/sync-ai-image-error-rules.js from shared/ai-image-error-rules.json.
// Do not edit this file directly.

const AI_IMAGE_ERROR_RULE_SOURCE = ${JSON.stringify(ruleSource, null, 2)};

function makePattern(name) {
  const pattern = AI_IMAGE_ERROR_RULE_SOURCE.patterns[name];
  return new RegExp(pattern.source, pattern.flags || '');
}

const MODERATION_ERROR_PATTERN = makePattern('moderation');
const INVALID_REQUEST_PATTERN = makePattern('invalidRequest');
const NAMED_PATTERNS = {
  moderation: MODERATION_ERROR_PATTERN,
  invalidRequest: INVALID_REQUEST_PATTERN
};

function includesAny(text, values) {
  return (values || []).some(value => text.includes(value));
}

function lowerIncludesAny(lower, values) {
  return (values || []).some(value => lower.includes(value));
}

function matchesScope(rule, scope) {
  return !rule.scopes || !rule.scopes.length || rule.scopes.includes(scope);
}

function matchesRequiredOptions(rule, options) {
  const required = rule.requiredOptions || {};
  return Object.keys(required).every(key => Boolean(options[key]) === Boolean(required[key]));
}

function matchesNamedPattern(text, patternNames) {
  return (patternNames || []).some(name => NAMED_PATTERNS[name] && NAMED_PATTERNS[name].test(text));
}

function matchesRule(rule, text, lower, options) {
  if (!matchesScope(rule, options.scope || 'shared')) return false;
  if (!matchesRequiredOptions(rule, options)) return false;
  return (
    includesAny(text, rule.includesAny) ||
    lowerIncludesAny(lower, rule.lowerIncludesAny) ||
    matchesNamedPattern(text, rule.patternNames)
  );
}

function formatAiImageSharedErrorMessage(message, fallback, options = {}) {
  const text = String(message || '').trim();
  if (!text) return fallback;

  const lower = text.toLowerCase();
  for (const rule of AI_IMAGE_ERROR_RULE_SOURCE.rules) {
    if (matchesRule(rule, text, lower, options)) {
      return rule.result;
    }
  }

  if (options.unknown === 'text60') {
    return text.length > 60 ? text.slice(0, 60) : text;
  }
  return fallback;
}

module.exports = {
  AI_IMAGE_ERROR_RULE_SOURCE,
  MODERATION_ERROR_PATTERN,
  INVALID_REQUEST_PATTERN,
  formatAiImageSharedErrorMessage
};
`;
}

function sync() {
  const checkOnly = process.argv.includes('--check');
  const generated = buildGeneratedModule(readRuleSource());
  const stale = [];

  for (const relativePath of targetPaths) {
    const targetPath = path.join(rootDir, relativePath);
    const current = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : '';
    if (current === generated) continue;

    if (checkOnly) {
      stale.push(relativePath);
      continue;
    }

    fs.mkdirSync(path.dirname(targetPath), { recursive: true });
    fs.writeFileSync(targetPath, generated);
    console.log(`synced ${relativePath}`);
  }

  if (stale.length) {
    console.error(`AI image error rule generated files are stale:\\n- ${stale.join('\\n- ')}`);
    process.exit(1);
  }

  if (checkOnly) {
    console.log('AI image error rule generated files are in sync');
  }
}

sync();
